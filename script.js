function setup() {
    frameRate(2);
    background('#acacac');
    for (var y = 0; y < matrix.length; y++) {

        for (var x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 1) {
                console.log(1);
                grassArr.push(new Grass(x, y))
            }
            else if (matrix[y][x] == 2) {
                grassEaterArr.push(new GrassEater(x, y))
            }
            else if (matrix[y][x] == 3) {
                wildArr.push(new Wild(x, y))
            }
            else if (matrix[y][x] == 4) {
                moverArr.push(new Mover(x, y))
            }
            else if (matrix[y][x] == 5) {
                doubleArr.push(new Double(x, y))
            }
        }
    }
    createCanvas(matrix.length * side, matrix.length * side);
}
function draw() {
    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {

            if (matrix[y][x] == 1) {
                fill("green");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 0) {
                fill("#acacac");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 2) {
                fill("yellow");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 3) {
                fill("red");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 4) {
                fill("orange");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 5) {
                fill("#9933ff");
                rect(x * side, y * side, side, side);
            }
        }
    }
    for (var i in grassArr) {
        grassArr[i].mul();
    }
    for (var i in grassEaterArr) {
        grassEaterArr[i].eat();
    }
    for (var i in wildArr) {
        wildArr[i].EatG();
    }
    for (var i in moverArr) {
        moverArr[i].moveEat();
    }
    for (var i in doubleArr) {
        doubleArr[i].doubEat();
    }

}
function matrixo(m) {
    var matrix = [];
    for (var i = 0; i < m; i++) {
        matrix.push([]);
        for (var j = 0; j < m; j++) {
            matrix[i][j] = Math.floor(Math.random() * 3);
        }
        for (var j = 0; j < m; j++) {
            matrix[i][j] = Math.floor(Math.random() * 4);
        }
        for (var j = 0; j < m; j++) {
            matrix[i][j] = Math.floor(Math.random() * 5);
        }
        for (var j = 0; j < m; j++) {
            matrix[i][j] = Math.floor(Math.random() * 6);
        }
    }
    return matrix;
}

